# Задача 2        
# Напишите программу, которая принимает на вход число N и выдает набор произведений 
# чисел от 1 до N. Факториал
# 5! = 120


def ex2_factorial():

    N = int(input())
    mul=1
    while(N>0):
        mul*=N
        N-=1
    print(f"Набор произведений равен {mul}")
    
# ex2_factorial()


# Задача 3

# Задана последовательность натуральных чисел, завершающаяся числом 0. 
# Требуется определить значение второго по величине элемента в этой последовательности, 
# то есть элемента, который будет наибольшим, если из последовательности удалить наибольший элемент.
# Пример:
# 1
# 7
# 9
# 0
# Вывод:
# 7

def ex3_max2():

    N = int(input())
    max = N
    max2 = 0

    while(N!=0):

        if N>max:
            max2=max
            max=N
        if N>max2 and N<max:
            max2 = N
        
        N = int(input())


    print(f"Первое наибольшее число равно {max}")
    print(f"Второе наибольшее число равно {max2}")


