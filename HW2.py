# Задача 1
# Напишите программу, которая принимает на вход вещественное число и показывает сумму его цифр.

# Пример:

# 6782 -> 23
# 0,56 -> 11


def ex1_sum_float():   # Математический способ, без преобразований строки работает некорректно 
    number = int(input().replace(".", ""))  # Не понял как избавиться от погрешности в числах флоат, когда
                             # после кучи нулей появляется цифра
    sum = 0
    while (number//10>0):
        sum += int(number%10)
        number = number//10
       
    sum += number
    print(f"Сумма цифр в числе равна {sum}")

