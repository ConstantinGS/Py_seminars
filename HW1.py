# Напишите программу, которая принимает на вход координаты двух точек 
# и находит расстояние между ними в 2D пространстве.

# Пример:

# - A (3,6); B (2,1) -> 5,09
# - A (7,-5); B (1,-1) -> 7,21

def Ex5_distance_of_Points():

    import math
    print("Введите координаты первой точки x и y: ")
    x1 = int (input())
    y1 = int (input())
    print("Введите координаты второй точки x и y: ")
    x2 = int (input())
    y2 = int (input())

    distance = math.sqrt(((x2-x1)**2)+((y2-y1)**2))
    print (f"Расстояние между ними равно: {distance} ")

