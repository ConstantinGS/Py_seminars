# Кузнечик прыгает по столбикам, расположенным на одной линии на равных 
# расстояниях друг от друга. Столбики имеют порядковые номера от 1 до N . 
# В начале Кузнечик сидит на столбике с номером 1. Он может прыгнуть на следующий
# столбик или сразу на второй столбик, считая от текущего. Требуется найти количество
# способов, которыми Кузнечик может добраться до столбика с номером N. 
# Учитывайте, что Кузнечик не может прыгать назад.


N = int (input())

def grasshopper(columns):

    if columns == 3: 
        return 2
    elif columns == 2: 
        return 1
    elif columns < 2: 
        return 0
    else:
        return grasshopper(columns-2)+grasshopper(columns-1)

print(f"У кузнечика {grasshopper(N)} способов добраться до {N} ступеньки")

    





