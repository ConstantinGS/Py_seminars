# Задача 1

# Напишите программу, которая принимает на вход цифру, обозначающую день недели, 
# и проверяет, является ли этот день выходным.

# Пример:

# - 6 -> да
# - 7 -> да
# - 1 -> нет

def Ex1_Weekends():
    print("Для определения дня недели введите число от 1 до 7: ")
    day = int(input())
    while (day not in range (1,8)):
        print("Некорректное число. Введите число от 1 до 7: ")
        day = int(input())

    if day == 6 or day == 7:
        print("День выходной")

    else:
        print("День рабочий")

